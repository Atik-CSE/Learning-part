marks = float(input("Enter merks : "))
if 80 <= marks <= 100:
    print("A+")
elif 70 <= marks <= 100:
    print("A")
elif 60 <= marks <= 100:
    print("A-")
elif 50 <= marks <= 100:
    print("B")
elif 40 <= marks <= 100:
    print("C")
elif 33 <= marks <= 100:
    print("D")
else:
    print("F")
